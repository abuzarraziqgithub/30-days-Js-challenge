//* Array Methods (Basic)
const numArray = [1, 2, 3, 4, 5];

//* Use the push method to add a new number to the end of the array and log the result to the console.
numArray.push(20);
console.log(numArray);

//* use the pop method to remove the last element from the array and log the result to the console.
numArray.pop();
console.log(numArray);

//* Use the shift method to remove the first element from the array and log the result to the console.
numArray.shift();
console.log(numArray);

//* Use the unshift method to add a new number to the beginning of the array and log the result to console.
numArray.unshift(15);
console.log(numArray);
